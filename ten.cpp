#include <iostream>
using namespace std;
//Relational  operator
int main()
{
   int a =10;
   int b=20;
   cout<<(a<b)<<endl;    //less than
   cout<<(a>b)<<endl;   //greater than 
   cout<<(a<=b)<<endl;    //less than or eual too
   cout<<(a>=b)<<endl;     //greater than or equal too 
   cout<<(a==b)<<endl;     //equal too
    cout<<(a!=b)<<endl;   //!= not equal too



}